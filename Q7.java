public class Q7 {
    public static void main(String[] args) {
        float fl = 123.456f;
        int num = (int) fl;
        

        System.out.println("Floating-point number: " + fl);
        System.out.println("Converted to integer: "+ num);

        
        int num1 = 789;
        float fl2 = (float) num1;

        System.out.println("Integer number: " + num1);
        System.out.println("Converted to floating-point: " + fl2);
    }
}
